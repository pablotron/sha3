  uint64_t t[25] = { 0 };
  memcpy(t, a, sizeof(t));
  a[0] = t[0] ^ (~t[1] & t[2]);
  a[1] = t[1] ^ (~t[2] & t[3]);
  a[2] = t[2] ^ (~t[3] & t[4]);
